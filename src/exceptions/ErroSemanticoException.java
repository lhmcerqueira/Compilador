package exceptions;

public class ErroSemanticoException extends Exception {

	public ErroSemanticoException() {
	
	}

	public ErroSemanticoException(String arg0) {
		super(arg0);
	}

	public ErroSemanticoException(Throwable arg0) {
		super(arg0);
	}

	public ErroSemanticoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ErroSemanticoException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

}
